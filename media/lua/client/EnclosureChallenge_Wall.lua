--[[
require "lua_timers"
EnclosureChallenge = EnclosureChallenge or {}

local WALL_SPRITE = "preset_depthmaps_01_7"
-----------------------            ---------------------------

function EnclosureChallenge.spawnWall(sq)
    if not sq then return end
    local wall = IsoThumpable.new(getCell(), sq, WALL_SPRITE, false, nil)
    wall:setCanBarricade(false)
    wall:setBlockAllTheSquare(true)
    wall:setCanPassThrough(false)
    sq:AddTileObject(wall)
    if isClient() then
        wall:transmitCompleteItemToServer()
    end
    ISInventoryPage.dirtyUI()
end

function EnclosureChallenge.doSledge(obj)
    if not obj then return end
    local sq = obj:getSquare()
    if isClient() then
        sledgeDestroy(obj)
    else
        if sq then
            sq:RemoveTileObject(obj)
            sq:getSpecialObjects():remove(obj)
            sq:getObjects():remove(obj)
        end
    end
    if sq then
        sq:transmitRemoveItemFromSquare(obj)
    end
end


-----------------------            ---------------------------
function EnclosureChallenge.setWalls(isSpawn)
    local pl = getPlayer()
    local size = EnclosureChallenge.EnclosureSize
    local x, y, z = round(pl:getX()), round(pl:getY()), 0
    local startX = math.floor(x / size) * size
    local startY = math.floor(y / size) * size

    local edgeSquares = EnclosureChallenge.getEnclosurEdgeSquares(startX, startY)
    for _, eSq in ipairs(edgeSquares) do
        local sq = getCell():getOrCreateGridSquare(eSq.x, eSq.y, z)
        if sq then
            local wall = EnclosureChallenge.getWall(sq)
            if isSpawn and not wall then
                EnclosureChallenge.spawnWall(sq)
            elseif not isSpawn and wall then
                EnclosureChallenge.doSledge(wall)
            end
        end
    end
end
-----------------------            ---------------------------
function EnclosureChallenge.isWall(sprName)
    return sprName == WALL_SPRITE
end

function EnclosureChallenge.getWall(sq)
    if not sq then return nil end
    for i = 0, sq:getObjects():size() - 1 do
        local obj = sq:getObjects():get(i)
        if EnclosureChallenge.isWall(EnclosureChallenge.getSprName(obj)) then
            return obj
        end
    end
    return nil
end
-----------------------            ---------------------------

function EnclosureChallenge.getSpr(obj)
    return obj and obj:getSprite() or nil
end

function EnclosureChallenge.getSprName(obj)
    local spr = EnclosureChallenge.getSpr(obj)
    return spr and spr:getName() or nil
end

