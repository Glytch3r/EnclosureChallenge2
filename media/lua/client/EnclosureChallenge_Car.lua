EnclosureChallenge = EnclosureChallenge or {}


function EnclosureChallenge.getCar(sq)
	local pl = getPlayer();
	sq = sq or pl:getSquare()
	local car = nil;
	if pl:getVehicle() then
		car = pl:getVehicle();
	elseif  sq ~= nil then
		car = IsoObjectPicker.Instance:PickVehicle(sq:getX(), sq:getY()) or sq:getVehicleContainer()
	elseif pl:getNearVehicle() then
		car = pl:getNearVehicle();
	elseif pl:getUseableVehicle() then
		car = pl:getUseableVehicle();
	end
	return car
end

function EnclosureChallenge.carHandler(pl)
	if not EnclosureChallenge.isChallenger() then return end
	local sq = pl:getSquare()
	local car = EnclosureChallenge.getCar(sq)
	if car then
        if  SandboxVars.EnclosureChallenge.LeakBattery then
            local item = car:getPartById("Battery")
            if item then
                local batt = item:getInventoryItem()
                if batt and batt:getUsedDelta() ~= 0 then
                    batt:setUsedDelta(0)
                end
            end
        end
        if SandboxVars.EnclosureChallenge.StopCar then
            if car:isEngineRunning() then
                ISVehicleMenu.onShutOff(pl)
            end
		end
	end
end
Events.OnEnterVehicle.Add(EnclosureChallenge.carHandler)
function EnclosureChallenge.forceExitCar()
    local pl = getPlayer()
	if not pl then return end
	local car = pl:getVehicle()
	if not car then return end
	local seat = car:getSeat(pl)
	car:exit(pl)
	car:setCharacterPosition(pl, seat, "outside")
	pl:PlayAnim("Idle")
	triggerEvent("OnExitVehicle", pl)
    car:updateHasExtendOffsetForExitEnd(pl)
end
