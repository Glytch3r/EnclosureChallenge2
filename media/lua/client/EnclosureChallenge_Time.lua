function EnclosureChallenge.getSec()
    local cal = PZCalendar and PZCalendar.getInstance and PZCalendar.getInstance()
    if cal and Calendar and Calendar.SECOND then
        return cal:get(Calendar.SECOND)
    end
    return nil
end

