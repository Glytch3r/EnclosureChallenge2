EnclosureChallenge = EnclosureChallenge or {}





-----------------------            ---------------------------
function EnclosureChallenge.doQuit(isRemote)
   if isRemote then
      EnclosureChallenge.setChallenge(false, true)
      EnclosureChallenge.clearChallengeData()
   else
      EnclosureChallenge.setChallenge(false, false)
      EnclosureChallenge.clearChallengeData()
   end
end
function EnclosureChallenge.isShouldSpawnItem()
   if not EnclosureChallenge.isChallenger() then return false, false end
   local remote = EnclosureChallenge.isRemoteMode() and  SandboxVars.EnclosureChallenge.RemoteItemReward
   local additive = EnclosureChallenge.isAdditiveMode() and SandboxVars.EnclosureChallenge.AdditiveItemReward
   return remote, additive
end





function EnclosureChallenge.doWin()
   local pl = getPlayer(); if not pl then return end
   local ec = EnclosureChallenge.getData(); if not ec then return end

   EnclosureChallenge.doReward()
   if EnclosureChallenge.isAdditiveMode() then
      local reward = SandboxVars.EnclosureChallenge.UnlockPointsReward or 1
      ec.UnlockPoints = (ec.UnlockPoints or 0) + reward
      HaloTextHelper.addTextWithArrow(pl, "Unlock Points + " .. tostring(reward), true, HaloTextHelper.getColorGreen())
   end

   if  EnclosureChallenge.isRemoteMode() then
      EnclosureChallenge.storeConquered(ec.RemoteChallenge)
      EnclosureChallenge.setMarkers(pl, false)
      EnclosureChallenge.clearChallengeData()
   end
end

function EnclosureChallenge.clearChallengeData()
   local ec = EnclosureChallenge.getData()
   if not ec then return end

   Events.EveryHours.Remove(EnclosureChallenge.RemoteTimer)
   Events.EveryHours.Remove(EnclosureChallenge.AdditiveTimer)

   ec.RemoteChallenge   = ""
   ec.RemoteTime        = 0
   ec.AdditiveChallenge = ""
   ec.AdditiveTime      = 0
   ec.AdditiveWins      = ec.AdditiveWins or 0

   EnclosureChallenge.delReturnPointMarker()
   EnclosureChallenge.clearRebound()
end

-----------------------            ---------------------------
Events.OnPlayerDeath.Add(function()
   local pl = getPlayer()
   if not EnclosureChallenge.isChallenger() then return end

   if EnclosureChallenge.isShouldAnnounce() then
      local enc = EnclosureChallenge.getEnclosureXY(pl:getX(), pl:getY())
      if enc then
         local encStr = EnclosureChallenge.getEnclosureStr(pl)
         local posStr = SandboxVars.EnclosureChallenge.CoordNotif and ": [" .. tostring(encStr) .. "]" or ""
         local msg = pl:getUsername() .. " " .. getText("ContextMenu_EnclosureChallenge_Fail") .. "  " .. posStr
         if isClient() then
               processGeneralMessage(msg)
         else
               pl:setHaloNote(msg, 150, 250, 150, 900)
         end
      end
   end

   EnclosureChallenge.clearChallengeData()
end)



