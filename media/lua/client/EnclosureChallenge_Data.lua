EnclosureChallenge = EnclosureChallenge or {}

function EnclosureChallenge.getData()
    local pl = getPlayer()
    if not isIngameState() or not pl then return nil end

    local md = pl:getModData()
    if not md.EnclosureChallenge then
        EnclosureChallenge.initChallengeData(pl)
    end
    return md.EnclosureChallenge
end

function EnclosureChallenge.getGUISettings()
    local ec = EnclosureChallenge.getData()
    if not ec then return {} end  -- fail-safe: never return nil
    ec.GUI = ec.GUI or {}
    return ec.GUI
end


-----------------------            ---------------------------
function EnclosureChallenge.resetData()
	local pl = getPlayer()
	if not pl then return end

	EnclosureChallenge.clearRebound()
	pl:getModData().EnclosureChallenge = nil
	EnclosureChallenge.initChallengeData(pl)

end


-----------------------            ---------------------------
function EnclosureChallenge.clearRebound() -- also removes markers
    local ec = EnclosureChallenge.getData()
    if not ec or not ec.Rebound then return end
    ec.Rebound = {}
    EnclosureChallenge.delReturnPointMarker()
end

function EnclosureChallenge.storeRebound(targ) -- also sets markers
   local pl = getPlayer()
   targ = targ or pl
   if not targ then return end

   EnclosureChallenge.clearRebound()

   local ec = EnclosureChallenge.getData()
   if not ec then return end
   local encStr =  EnclosureChallenge.getEnclosureStr(targ)

   ec.Rebound = {
      x = round(targ:getX()),
      y = round(targ:getY()),
      z = targ:getZ() or 0,
   }
   EnclosureChallenge.setReturnPointMarker()

end
-----------------------            ---------------------------

-----------------------            ---------------------------
function EnclosureChallenge.getPoints()
    local ec = EnclosureChallenge.getData()
    if not ec then return nil end
    ec.UnlockPoints      = ec.UnlockPoints      or SandboxVars.EnclosureChallenge.StartingUnlockPoints or 1

    return ec.UnlockPoints
end

function EnclosureChallenge.whereami()
    local pl = getPlayer()
    local x, y, z = math.ceil(pl:getX()), math.ceil(pl:getY()), pl:getZ()
    local encVar = EnclosureChallenge.getEnclosure(pl)
    local encStr = tostring(encVar.x)..", "..tostring(encVar.y)
    return tostring(x)..", "..tostring(y)..", "..tostring(z).."\n"..tostring(encStr)
end

