EnclosureChallenge = EnclosureChallenge or {}

function EnclosureChallenge.resetData()
	local pl = getPlayer()
	if not pl then return end

	EnclosureChallenge.clearRebound()
	pl:getModData().EnclosureChallenge = nil
	EnclosureChallenge.initChallengeData(pl)
end

function EnclosureChallenge.getData()
    local pl = getPlayer()
    if not pl then return nil end
    local data = pl:getModData()
    data.EnclosureChallenge = data.EnclosureChallenge or {}
    return data.EnclosureChallenge
end

function EnclosureChallenge.getPoints()
    local ec = EnclosureChallenge.getData()
    if not ec then return nil end
    return ec.UnlockPoints or 0
end

function EnclosureChallenge.whereami()
    local pl = getPlayer()
    local x, y, z = math.ceil(pl:getX()), math.ceil(pl:getY()), pl:getZ()
    local encVar = EnclosureChallenge.getEnclosure(pl)
    local encStr = tostring(encVar.x)..", "..tostring(encVar.y)
    return tostring(x)..", "..tostring(y)..", "..tostring(z).."\n"..tostring(encStr)
end

